#include "libft.h"

int	ft_isalnum(int c)
{
	while ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'))
	{
		return (c);
	}
	return (0);
}
